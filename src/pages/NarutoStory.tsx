const NarutoStory = () => {
  const [currentPage, setCurrentPage] = useState(0);
  const [isFlipping, setIsFlipping] = useState(false);
  const [flipDirection, setFlipDirection] = useState<"next" | "prev">("next");

  const totalPages = pages.length;

  const goToPage = useCallback(
    (target: number) => {
      if (isFlipping || target < 0 || target >= totalPages || target === currentPage) return;
      setFlipDirection(target > currentPage ? "next" : "prev");
      setIsFlipping(true);
      setTimeout(() => {
        setCurrentPage(target);
        setIsFlipping(false);
      }, 600);
    },
    [isFlipping, currentPage, totalPages]
  );

  const nextPage = useCallback(() => goToPage(currentPage + 1), [goToPage, currentPage]);
  const prevPage = useCallback(() => goToPage(currentPage - 1), [goToPage, currentPage]);

  // Keyboard navigation
  useEffect(() => {
    const handler = (e: KeyboardEvent) => {
      if (e.key === "ArrowRight" || e.key === " ") {
        e.preventDefault();
        nextPage();
      }
      if (e.key === "ArrowLeft") {
        e.preventDefault();
        prevPage();
      }
    };
    window.addEventListener("keydown", handler);
    return () => window.removeEventListener("keydown", handler);
  }, [nextPage, prevPage]);

  // Touch swipe
  useEffect(() => {
    let startX = 0;
    const onStart = (e: TouchEvent) => { startX = e.touches[0].clientX; };
    const onEnd = (e: TouchEvent) => {
      const diff = startX - e.changedTouches[0].clientX;
      if (Math.abs(diff) > 50) {
        diff > 0 ? nextPage() : prevPage();
      }
    };
    window.addEventListener("touchstart", onStart);
    window.addEventListener("touchend", onEnd);
    return () => {
      window.removeEventListener("touchstart", onStart);
      window.removeEventListener("touchend", onEnd);
    };
  }, [nextPage, prevPage]);

  const page = pages[currentPage];

  const renderPage = () => {
    switch (page.type) {
      case "cover":
        return (
          <div className="naruto-page-inner naruto-cover">
            <div className="naruto-cover-spiral" />
            <KonohaLeaf className="w-20 h-20 naruto-leaf-emblem" />
            <h1 className="naruto-cover-title">{page.title}</h1>
            <p className="naruto-cover-subtitle">{page.subtitle}</p>
            <span className="naruto-cover-year">{page.year}</span>
            <span className="naruto-cover-author">by FairyTeller</span>
          </div>
        );
      case "annotation":
        return (
          <div className="naruto-page-inner naruto-annotation">
            <KonohaLeaf className="w-10 h-10 naruto-leaf-small" />
            <p className="naruto-annotation-text">{page.text}</p>
          </div>
        );
      case "toc":
        return (
          <div className="naruto-page-inner naruto-toc">
            <h2 className="naruto-toc-title">ОГЛАВЛЕНИЕ</h2>
            <KonohaLeaf className="w-8 h-8 naruto-leaf-small" />
            <ul className="naruto-toc-list">
              {page.chapters.map((ch, i) => (
                <li key={i} className="naruto-toc-item" onClick={() => goToPage(ch.page)}>
                  <span className="naruto-toc-name">{ch.title}</span>
                  <span className="naruto-toc-dots" />
                  <span className="naruto-toc-page">{ch.page + 1}</span>
                </li>
              ))}
            </ul>
          </div>
        );
      case "chapter-title":
        return (
          <div className="naruto-page-inner naruto-chapter-title">
            <div className="naruto-chapter-ornament-top" />
            <span className="naruto-chapter-label">Глава {page.chapter}</span>
            <h2 className="naruto-chapter-name">{page.title}</h2>
            <div className="naruto-chapter-ornament-bottom" />
          </div>
        );
      case "text":
        return (
          <div className="naruto-page-inner naruto-text-page">
            <div className="naruto-text-content">
              {page.content.split("\n\n").map((p, i) => (
                <p key={i} className="naruto-paragraph">{p}</p>
              ))}
            </div>
            <span className="naruto-page-number">{currentPage + 1}</span>
          </div>
        );
      case "final":
        return (
          <div className="naruto-page-inner naruto-final">
            <KonohaLeaf className="w-16 h-16 naruto-leaf-emblem" />
            <p className="naruto-final-text">{page.text}</p>
          </div>
        );
      default:
        return null;
    }
  };

  return (
    <div className="naruto-story-root">
      <FloatingLeaves />

      {/* Book container */}
      <div className="naruto-book-container">
        <div
          className={`naruto-page ${isFlipping ? `naruto-flip-${flipDirection}` : ""}`}
        >
          {renderPage()}
        </div>
      </div>

      {/* Navigation */}
      <div className="naruto-nav">
        <button
          onClick={prevPage}
          disabled={currentPage === 0 || isFlipping}
          className="naruto-nav-btn"
          aria-label="Previous page"
        >
          <ChevronLeft className="w-5 h-5" />
        </button>

        <span className="naruto-nav-counter">
          {currentPage + 1} / {totalPages}
        </span>

        <button
          onClick={nextPage}
          disabled={currentPage === totalPages - 1 || isFlipping}
          className="naruto-nav-btn"
          aria-label="Next page"
        >
          <ChevronRight className="w-5 h-5" />
        </button>
      </div>
    </div>
  );
};

export default NarutoStory;
